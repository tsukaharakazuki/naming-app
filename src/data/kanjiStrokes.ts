/**
 * Look up the stroke count for a single kanji character.
 * Returns null if the character is not found.
 */
export function getStrokeCount(char: string): number | null {
  return kanjiStrokes[char] ?? null;
}

/**
 * Get an array of stroke counts for each character in a surname.
 * Returns null if any character is not found.
 */
export function getSurnameStrokes(surname: string): number[] | null {
  const strokes: number[] = [];
  for (const char of surname) {
    const s = getStrokeCount(char);
    if (s === null) return null;
    strokes.push(s);
  }
  return strokes;
}
